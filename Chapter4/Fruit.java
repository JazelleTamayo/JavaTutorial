public class Fruit {
   String fruit;
   double weight;
   double price;
   
   public void setFruit(String aFruit) {
      fruit = aFruit;
   }
   
   public void setWeight(double aWeight) {
      weight = aWeight;
   }
   
   public void setPrice(double aPrice) {
      price = aPrice;
   }
   
   public String getFruit() {
      return fruit;
   }
   
   public double getWeight() {
      return weight;
   }
   
   public double getPrice() {
      return price;
   }
}