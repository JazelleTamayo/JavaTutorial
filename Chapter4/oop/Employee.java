public class Employee {
   private int empId;
   private double empSal;
   
   public void setEmpNum(int id) {
      empId = id;
   
   }
   
   public int getEmpNum() {
      return empId;
   
   }
   
   public void setEmpSalary(double sal) {
      empSal = sal;
   
   }
   
   public double getEmpSalary() {
      return empSal;
   
   }

}